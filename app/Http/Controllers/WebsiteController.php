     */
    public function store(Request $request)
    {
        $website = new website();
        $website->title = $request->title;
        $website->custom_domain = $request->custom_domain;
        $website->user_id = Auth::user()->id;
        $website->save();
        return redirect()->route('websites.index');
    }


